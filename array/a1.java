// arrays are data type in which a list of variables with similar type of data can be stored

public class a1 {

    public static void main(String[] args) {
        int[] marks = new int[3];
        marks[0] = 97;
        marks[1] = 98;
        marks[2] = 95;

        // System.out.println(marks[0]);
        for (int i = 0; i <= marks[i]; i++) {
            System.out.println(marks[i]);
        }
    }
}
